function min(a, b){

    if(a < b){
        return a;
    }
    if(a > b){
        return b;
    } 
    else{
        return null;
    }
}

function max(a, b){

    if(a < b){
        return b;
    }
    if(a > b){
        return a;
    } 
    else{
        return null;
    }
}